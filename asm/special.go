func genSqrt_F64() {

	TEXT("Sqrt_AVX2_F64", NOSPLIT, "func(x []float64) float64")
	Pragma("noescape")
	Load(Param("x").Base(), RDI)
	Load(Param("x").Len(), RSI)

	TESTQ(RSI, RSI)
	JE(LabelRef("LBB0_7"))
	CMPQ(RSI, Imm(4))
	JAE(LabelRef("LBB0_3"))
	XORL(EAX, EAX)
	JMP(LabelRef("LBB0_6"))

	Label("LBB0_3")
	{
		MOVQ(RSI, RAX)
		ANDQ(I32(-4), RAX)
		XORL(ECX, ECX)
	}

	Label("LBB0_4")
	{
		VSQRTPD(Mem{Base: RDI}.Idx(RCX, 8), Y0)
		VMOVUPD(Y0, Mem{Base: RDI}.Idx(RCX, 8))
		ADDQ(Imm(4), RCX)
		CMPQ(RAX, RCX)
		JNE(LabelRef("LBB0_4"))
		CMPQ(RAX, RSI)
		JE(LabelRef("LBB0_7"))
	}

	Label("LBB0_6")
	{
		VMOVSD(Mem{Base: RDI}.Idx(RAX, 8), X0)
		VSQRTSD(X0, X0, X0)
		VMOVSD(X0, Mem{Base: RDI}.Idx(RAX, 8))
		ADDQ(Imm(1), RAX)
		CMPQ(RSI, RAX)
		JNE(LabelRef("LBB0_6"))
	}

	Label("LBB0_7")
	{
		VZEROUPPER()
		Store(X0, ReturnIndex(0))
		RET()
	}
}

